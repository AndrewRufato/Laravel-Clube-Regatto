      <p>Regatto Ambientes Planejados</p>

      <section>
        <h2>1. OBJETO DO PROGRAMA</h2>
        <p>
          O presente Regulamento estabelece as normas e condições de participação no Clube Regatto, programa de relacionamento e
          bonificação destinado a profissionais parceiros que realizam indicações comerciais de clientes à Regatto Ambientes Planejados,
          com base em critérios de desempenho e conversão de vendas.
        </p>
        <p>
          O programa tem caráter exclusivamente promocional e de fidelização comercial, não configurando vínculo empregatício, societário,
          representação comercial ou qualquer outra relação jurídica além da parceria comercial.
        </p>
      </section>

      <section>
        <h2>2. ELEGIBILIDADE E PARTICIPAÇÃO</h2>
        <p>
          Poderão participar do Clube Regatto exclusivamente profissionais que atendam cumulativamente aos seguintes requisitos:
        </p>
        <ul>
          <li>
            I – Ser:
            <ul>
              <li>Arquiteto(a) com registro ativo no CAU;</li>
              <li>Engenheiro(a) com registro ativo no CREA;</li>
              <li>Corretor(a) de imóveis com registro ativo no CRECI;</li>
            </ul>
          </li>
          <li>II – Possuir registro profissional válido e regular em seu respectivo conselho de classe;</li>
          <li>III – Realizar cadastro prévio no programa;</li>
          <li>IV – Ter seu cadastro formalmente aprovado pela Regatto.</li>
        </ul>
        <p>
          A Regatto reserva-se o direito de solicitar, a qualquer tempo, comprovação documental do registro profissional.
        </p>
      </section>

      <section>
        <h2>3. CADASTRO E ACESSO AO PORTAL DO CLUBE</h2>
        <h3>3.1 O interessado deverá realizar cadastro prévio, fornecendo:</h3>
        <ul>
          <li>Nome completo</li>
          <li>CPF ou CNPJ</li>
          <li>Profissão</li>
          <li>Número do conselho profissional (CAU, CREA ou CRECI)</li>
          <li>Telefone e e-mail</li>
        </ul>

        <h3>3.2 Após análise e validação pela Regatto, o parceiro aprovado receberá:</h3>
        <ul>
          <li>Login e senha individuais</li>
          <li>Acesso ao Portal do Clube Regatto</li>
        </ul>

        <h3>3.3</h3>
        <p>
          O acesso ao portal é pessoal e intransferível, sendo vedado o compartilhamento de login e senha com terceiros.
        </p>
      </section>

      <section>
        <h2>4. VALIDADE DAS INDICAÇÕES</h2>
        <p>Para fins de pontuação no programa, somente serão consideradas válidas as indicações que:</p>
        <ul>
          <li>I – Sejam realizadas por parceiros previamente cadastrados e aprovados;</li>
          <li>II – Sejam informadas à Regatto antes do primeiro atendimento ao cliente;</li>
          <li>III – Refiram-se a clientes que não estejam previamente cadastrados na base comercial da empresa;</li>
          <li>IV – Resultem em contrato efetivamente firmado com a Regatto;</li>
          <li>V – Possuam confirmação de pagamento inicial do contrato.</li>
        </ul>
        <p>
          Indicações realizadas após o primeiro atendimento ou sem registro prévio poderão ser desconsideradas, a critério da Regatto.
        </p>
      </section>

      <section>
        <h2>5. CRITÉRIO DE PONTUAÇÃO</h2>
        <p>
          A pontuação do Clube Regatto será apurada com base no valor total dos contratos fechados provenientes de indicações válidas do
          parceiro, excluindo-se o custo financeiro da operação.
        </p>
        <p>Serão considerados apenas:</p>
        <ul>
          <li>Contratos formalizados</li>
          <li>Com pagamento inicial confirmado</li>
          <li>Sem cancelamento ou inadimplência relevante</li>
        </ul>
        <p>A Regatto poderá desconsiderar contratos cancelados, rescindidos ou judicializados.</p>
      </section>

      <section>
        <h2>6. PERÍODO DE APURAÇÃO DOS PONTOS</h2>
        <p>6.1 A contagem dos pontos será realizada anualmente, no período de:</p>
        <p><strong>01 de janeiro a 31 de dezembro de cada ano civil.</strong></p>

        <p>6.2 Os pontos:</p>
        <ul>
          <li>Não são cumulativos para o ano seguinte;</li>
          <li>Serão automaticamente zerados ao final do mês de dezembro.</li>
        </ul>

        <p>6.3 Caso, ao final do período anual:</p>
        <ul>
          <li>Haja saldo de pontos insuficiente para troca; ou</li>
          <li>O parceiro não realize o resgate dos prêmios disponíveis;</li>
        </ul>
        <p>
          os pontos remanescentes serão integralmente zerados, sem possibilidade de transferência, conversão em dinheiro ou reaproveitamento
          no exercício seguinte.
        </p>
      </section>

      <section>
        <h2>7. RESGATE DE PREMIAÇÕES</h2>
        <p>
          7.1 O parceiro poderá realizar quantas trocas desejar ao longo do ano, desde que atinja o valor mínimo correspondente a cada faixa
          de premiação vigente.
        </p>
        <p>7.2 Após cada resgate, a pontuação será deduzida conforme a faixa correspondente.</p>

        <p>7.3 As premiações:</p>
        <ul>
          <li>Não possuem natureza salarial ou remuneratória;</li>
          <li>Não são conversíveis em dinheiro, salvo quando expressamente previsto;</li>
          <li>Estão sujeitas à disponibilidade de estoque e fornecedores.</li>
        </ul>
        <p>
          A Regatto poderá substituir prêmios por itens de valor equivalente, sem prejuízo ao parceiro.
        </p>
      </section>

      <section>
        <h2>8. NATUREZA PROMOCIONAL E RESPONSABILIDADE LEGAL</h2>
        <p>8.1 O Clube Regatto possui caráter promocional e institucional, não configurando:</p>
        <ul>
          <li>Comissão imobiliária;</li>
          <li>Pagamento por intermediação profissional;</li>
          <li>Relação de representação comercial;</li>
          <li>Sociedade ou vínculo empregatício.</li>
        </ul>

        <p>
          8.2 O parceiro é integralmente responsável por sua atuação profissional perante seus respectivos conselhos de classe e órgãos reguladores.
        </p>

        <p>
          8.3 A participação no programa não autoriza o uso da marca Regatto sem autorização formal e expressa.
        </p>
      </section>

      <section>
        <h2>9. CONDUTAS VEDADAS</h2>
        <p>Serão consideradas infrações ao regulamento:</p>
        <ul>
          <li>Indicação de clientes fictícios ou duplicados;</li>
          <li>Tentativa de fraude na origem da indicação;</li>
          <li>Compartilhamento indevido de login do portal;</li>
          <li>Uso indevido da marca Regatto;</li>
          <li>Práticas comerciais antiéticas ou que comprometam a imagem da empresa.</li>
        </ul>

        <p>Em caso de irregularidade, a Regatto poderá:</p>
        <ul>
          <li>Suspender o parceiro;</li>
          <li>Cancelar pontuações;</li>
          <li>Excluir definitivamente do programa.</li>
        </ul>
      </section>

      <section>
        <h2>10. ALTERAÇÕES DO PROGRAMA</h2>
        <p>A Regatto reserva-se o direito de:</p>
        <ul>
          <li>Alterar regras de pontuação;</li>
          <li>Atualizar faixas de premiação;</li>
          <li>Modificar critérios operacionais do programa;</li>
        </ul>
        <p>mediante comunicação prévia aos parceiros participantes.</p>
      </section>

      <section>
        <h2>11. PROTEÇÃO DE DADOS</h2>
        <p>Os dados fornecidos no cadastro serão utilizados exclusivamente para:</p>
        <ul>
          <li>Gestão do programa</li>
          <li>Comunicação institucional</li>
          <li>Controle de indicações e premiações</li>
        </ul>
        <p>Em conformidade com a Lei Geral de Proteção de Dados (LGPD – Lei nº 13.709/2018).</p>
      </section>

      <section>
        <h2>12. DISPOSIÇÕES FINAIS</h2>
        <p>A participação no Clube Regatto implica na aceitação integral deste Regulamento.</p>
        <p>
          Casos omissos serão analisados e decididos exclusivamente pela Regatto Ambientes Planejados, com base em critérios de boa-fé,
          transparência e ética comercial.
        </p>
      </section>
